package com.adrian.proyecto_invidentes.sqlite_modelo;

public class configuracion {
    String mi_telefono;
    Float mi_distancia;
    String mi_contacto;

    public configuracion() {

    }

    public String getMi_telefono() {
        return mi_telefono;
    }

    public void setMi_telefono(String mi_telefono) {
        this.mi_telefono = mi_telefono;
    }

    public Float getMi_distancia() {
        return mi_distancia;
    }

    public void setMi_distancia(Float mi_distancia) {
        this.mi_distancia = mi_distancia;
    }

    public String getMi_contacto() {
        return mi_contacto;
    }

    public void setMi_contacto(String mi_contacto) {
        this.mi_contacto = mi_contacto;
    }
}
